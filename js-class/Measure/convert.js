//1. ইঞ্চি থেকে ফিটে কনভার্সন (Inch to Feet Conversion):




// ইঞ্চি থেকে ফিট
function inchToFeet(inch) {
    return inch / 12;
  }
  
  console.log(inchToFeet(24)); // 2 feet
  
  // ফিট থেকে ইঞ্চি
  function feetToInch(feet) {
    return feet * 12;
  }
  
  console.log(feetToInch(5)); // 60 inches
  
  